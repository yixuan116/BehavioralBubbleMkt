def run_low_high_mc_test(df: pd.DataFrame, n_perm: int = 50_000) -> dict:
    """
    Monte Carlo permutation test comparing LOW_PRO (25%+50%) vs
    HIGH_PRO (75%+100%).

    Returns:
        {
            "T_obs": ...,
            "p_value": ...,
            "mean_low": ...,
            "mean_high": ...,
            "n_perm": ...,
            "t_null": ...  # null distribution for plotting
        }
    """
    low = df[df['pro_share'].isin(PRO_LOW)]
    high = df[df['pro_share'].isin(PRO_HIGH)]

    mean_low = low['Bubble'].mean()
    mean_high = high['Bubble'].mean()
    T_obs = mean_high - mean_low

    pro_labels = df['pro_share'].values
    bubble_values = df['Bubble'].values

    t_null = np.zeros(n_perm)
    for m in range(n_perm):
        permuted = np.random.permutation(pro_labels)
        low_mask = np.isin(permuted, list(PRO_LOW))
        high_mask = np.isin(permuted, list(PRO_HIGH))
        t_null[m] = bubble_values[high_mask].mean() - bubble_values[low_mask].mean()

    p_value = np.mean(t_null <= T_obs)

    return {
        "T_obs": T_obs,
        "p_value": p_value,
        "mean_low": mean_low,
        "mean_high": mean_high,
        "n_perm": n_perm,
        "t_null": t_null
    }


def _annotate_hist_bars(ax, fmt="{:.3f}", fontsize=7, threshold=0.001):
    """
    Helper function to annotate histogram bars with numeric values.
    
    Args:
        ax: Axes object containing the histogram
        fmt: Format string for the numeric values
        fontsize: Font size for the annotations
        threshold: Minimum height threshold for labeling bars
    """
    # Get histogram data from the axes
    for rect in ax.patches:
        height = rect.get_height()
        if height <= threshold:
            continue
        x = rect.get_x() + rect.get_width() / 2
        y = height
        ax.text(x, y + height * 0.05,
                fmt.format(height),
                ha="center", va="bottom",
                fontsize=fontsize, rotation=0, alpha=0.7, color="dimgray")


