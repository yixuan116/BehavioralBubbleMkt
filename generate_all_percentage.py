def compute_choiceb_relative_bubbles():
    """Return session-level relative mispricing (percentage) for Choice B.
    Uses absolute values: |Bubble %| (take absolute value first, then average)
    """
    raw = pd.read_csv(io.StringIO(EXPERIMENT_B_TRADING_CSV))
    period_prices = (
        raw.groupby(['Session', 'Period'], as_index=False)
        .agg({
            'LastPrice': 'last',
            'Fundamental': 'last',
            'ProShare': 'last'
        })
    )
    # Calculate Bubble % (relative/percentage)
    period_prices['BubblePct'] = ((period_prices['LastPrice'] - period_prices['Fundamental']) / period_prices['Fundamental']) * 100
    period_prices['BubblePctAbs'] = period_prices['BubblePct'].abs()
    session_summary = (
        period_prices.groupby('Session', as_index=False)
        .agg({
            'BubblePctAbs': 'mean',
            'ProShare': 'first'
        })
        .rename(columns={'BubblePctAbs': 'BubblePctMean'})
        .sort_values('Session')
    )
    return session_summary


